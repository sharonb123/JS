// declaring array
const fruit=     ["pineapple","orange","apple"];
const vegetables= ["tomato","potato","pumking"];



// length of array
alert(vegetables.length);


// joining the array
const Food= [fruit.join(vegetables)];
alert(Food);
    



//removing last 2 elements of array
// since it will be from index 4 and we want to remove  2 items so we put 2 as the other argument
alert(Food.slice(4,2));



// reversing array
alert(Food.reverse());



// remove first element
alert(Food.shift(Food));





